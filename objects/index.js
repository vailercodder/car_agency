const CarManager = {
  agencies,

  // Retrieve all cars available for purchase.
  // @return {object[]} - Array of cars
  getAllCars() {
    let carDetails = [];

    for (let agency of agencies) {
      for (let carBrand of agency.cars) {
        for (let model of carBrand.models) {
          carDetails.push({
            agencyName: agency.agencyName,
            brand: carBrand.brand,
            name: model.name,
            year: model.year,
            price: model.price,
            carNumber: model.carNumber,
          });
        }
      }
    }
    return carDetails;
  },

  // Search for cars based on certain criteria.
  // @param {number} year - The production year of the car
  // @param {number} price - The price of the car
  // @param {string} brand - The brand of the car
  // @return {object[]} - Array of cars that meet the criteria
  searchCars(year, price, brand) {
    let carDetails = [];

    for (let agency of agencies) {
      for (let carBrand of agency.cars) {
        if (carBrand.brand === brand) {
          for (let model of carBrand.models) {
            if (model.year === year && model.price <= price) {
              carDetails.push({
                agencyName: agency.agencyName,
                brand: carBrand.brand,
                name: model.name,
                year: model.year,
                price: model.price,
                carNumber: model.carNumber,
              });
            }
          }
        }
      }
    }
    return carDetails;
  },

  // Return the most expensive car available for sale.
  // @return {object} - The most expensive car
  getMostExpensiveCar() {
    let mostExpensiveCar = null;

    for (let agency of agencies) {
      for (let carBrand of agency.cars) {
        for (let model of carBrand.models) {
          if (!mostExpensiveCar || model.price > mostExpensiveCar.price) {
            mostExpensiveCar = model;
          }
        }
      }
    }
    return mostExpensiveCar;
  },

  // Return the cheapest car available for sale.
  // @return {object} - The cheapest car
  getCheapestCar() {
    let cheapestCar = null;

    for (let agency of agencies) {
      for (let carBrand of agency.cars) {
        for (let model of carBrand.models) {
          if (!cheapestCar || model.price < cheapestCar.price) {
            cheapestCar = model;
          }
        }
      }
    }

    return cheapestCar;
  },
};

